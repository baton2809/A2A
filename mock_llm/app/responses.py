"""
Генерация ответов для Mock LLM.
Ответы шаблонные — имитируют разные типы запросов.
"""

import random
import re


_PATTERNS = [
    (
        r"(?i)(объясни|explain|расскажи)\s+(.+)",
        lambda m: (
            f"## {m.group(2).strip().capitalize()}\n\n"
            "Рассмотрим этот вопрос с нескольких сторон.\n\n"
            "**Основная идея**: концепция строится на принципе разделения ответственности, "
            "где каждый компонент выполняет одну чётко определённую задачу.\n\n"
            "**Практическое применение**:\n"
            "- Упрощает тестирование и отладку\n"
            "- Облегчает масштабирование системы\n"
            "- Улучшает читаемость кода\n\n"
            "Если нужны подробности по конкретному аспекту — спрашивайте."
        ),
    ),
    (
        r"(?i)(напиши|write|создай|create)\s+(код|code|функцию|function|класс|class)\s+(.+)",
        lambda m: (
            f"Вот реализация для '{m.group(3).strip()}':\n\n"
            "```python\n"
            "from dataclasses import dataclass\n"
            "from typing import Any\n\n\n"
            "@dataclass\n"
            "class Processor:\n"
            "    name: str\n"
            "    config: dict[str, Any]\n\n"
            "    def run(self, data: list) -> list:\n"
            "        \"\"\"Обрабатывает входящие данные.\"\"\"\n"
            "        return [self._transform(item) for item in data if self._valid(item)]\n\n"
            "    def _transform(self, item: Any) -> Any:\n"
            "        return {\"id\": getattr(item, \"id\", None), \"value\": str(item)}\n\n"
            "    def _valid(self, item: Any) -> bool:\n"
            "        return item is not None\n"
            "```\n\n"
            "Код написан в стиле Python 3.10+. Добавьте типизацию под свою модель данных."
        ),
    ),
    (
        r"(?i)(сравни|compare)\s+(.+?)\s+(и|and|vs|versus)\s+(.+)",
        lambda m: (
            f"## Сравнение: {m.group(2).strip()} vs {m.group(4).strip()}\n\n"
            "| Критерий | Вариант A | Вариант B |\n"
            "|----------|-----------|----------|\n"
            "| Производительность | Высокая при малых данных | Лучше при объёмах >10k |\n"
            "| Сложность | Проще в реализации | Богаче функционально |\n"
            "| Экосистема | Зрелая, стабильная | Активно развивается |\n"
            "| Подходит для | Прототипов и малых проектов | Продакшн-нагрузок |\n\n"
            "**Вывод**: выбор зависит от масштаба задачи. При сомнениях начните с простого."
        ),
    ),
]

_FALLBACK = [
    (
        "Хороший вопрос. Вот что я думаю:\n\n"
        "Ключ к решению — разбить задачу на небольшие понятные шаги:\n\n"
        "1. Определите входные данные и ожидаемый результат\n"
        "2. Выберите наиболее простой рабочий подход\n"
        "3. Реализуйте минимальную рабочую версию\n"
        "4. Напишите тесты для краевых случаев\n"
        "5. Итеративно улучшайте\n\n"
        "Такой подход снижает когнитивную нагрузку и ускоряет получение результата."
    ),
    (
        "Отвечаю на ваш запрос:\n\n"
        "С точки зрения архитектуры, оптимальное решение обычно включает три уровня:\n\n"
        "- **Интерфейс** — определяет контракт взаимодействия\n"
        "- **Логика** — реализует бизнес-правила\n"
        "- **Хранилище** — отвечает за персистентность\n\n"
        "Соблюдение этого разделения упрощает как разработку, так и тестирование."
    ),
]


def build_reply(messages: list[dict]) -> str:
    """Генерирует ответ на основе последнего сообщения пользователя."""
    user_text = ""
    for msg in reversed(messages):
        if msg.get("role") == "user":
            user_text = msg.get("content", "")
            break

    if not user_text:
        return "Привет! Задайте вопрос — я отвечу."

    for pattern, handler in _PATTERNS:
        m = re.search(pattern, user_text)
        if m:
            return handler(m)

    return random.choice(_FALLBACK)


def approx_tokens(text: str) -> int:
    """Грубая оценка числа токенов (~4 символа на токен)."""
    return max(1, len(text) // 4)
